#relational operators are also know as comparision operators 
#Basic equality
print(10 == 10.0) #True
#String Comparison
print("apple" > "banana")#False
#Boolean
print(True == 1) #True
#Chained relation
print(5 < 10 > 7)#True
#Checking that int is equal to str
print(5 != "5")#True

#Here are some basics of relational operators
# "==" equals to 
# "!==" not equals to 
# "<" less than
# ">" Greater than 
# "<=" less than or equals
# ">=" Greater than or equals 
