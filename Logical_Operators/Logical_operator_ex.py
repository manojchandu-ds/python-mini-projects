#The logical operators are used to perform logical operations on boolean value
# They are 
# and,or,not 

#AND operator
print(True and True) #Tree
# Here "and" is useful when the two boolean values are true 
print(True and False) # False

#OR operator
print(True or False) #True
print(False or False) #False
#It satisfies when there is atleast one condition is true

#NOT operator
print(not(False)) #True
print(not(True)) #False
# Logical NOT Operator gives the opposite value

